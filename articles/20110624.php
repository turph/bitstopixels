		<h3>Right rail</h3>
		<p>For browser windows greater than 1024px we experimented with adding a vertical tower of related results, utilizing dead page space since the results container was fixed at 980px. Site metrics showed that an increasing number of visitors were using wider screens so there was opportunity.</p>
		<p>A/B testing discovered that while there was no impact on product grid interaction there was a significant placement blindness. CTR was extremely low so this placement was discarded in preference of the last row.</p>

		<h3>Conclusions</h3>
		<p>The addition of this placement and product offering to the ProductAds program greatly increase merchant participation and revenue. Two years after launch, the last row placement is still there (slightly different design) and the right rail has been reintroduced.</p>
