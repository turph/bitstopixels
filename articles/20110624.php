		<h4>Problem</h4>
		<p>Only unique products, marked for advertisement, are eligible to display inline within search results. Given that the vast majority of a merchant's catalog maps to existing products this poses a discovery challenge as their products will be suppressed. Therefore, merchants are willing to pay for greater placement of their relevant products somewhere on search results.</p>
		<h4>Solution</h4>
		<p>Space on search results is extremely limited and user intent is highly focused so injection of sponsored results within a grid of organic search was ruled out as being too obtrusive. As sponsored content, widgets and their results were considered additional content and were not seen as a main focal point. Two placements were explored:</p>
		<ul>
			<li>Last row on Category and Product search pages</li>
			<li>Right rail on Category search page</li>
		</ul>
		
		<h3>Last row</h3>
		<p>Placing the widget slot above pagination at the lost row of results accomplished several goals. Placement gave validity to the results and removed the perception that the widget was simply another ad unit. Also in the natural flow of the visitor, they have scanned down the entire results set and only then would they see alternative options.</p>
		<p>From a design perspective, the goal was to balance the need for separation and identification against the desire to feel associated with the site and not simply appended.</p>
		
		<h5>Chosen direction</h5>
		<p>After some A/B testing rounds between 3 variants, the metrics supported choosing a simple light gray wrapper. The treatment was enough to set the results apart from the organic flow but not interrupt the overall shopping experience. Engagement was within accepted values based on other placements so the program continued to display the widget.</p>
